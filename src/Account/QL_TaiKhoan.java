    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        try{
            String check = "Select TenTK from accountmanagement where TenTK = '" + txtTK.getText() + "'";
            PreparedStatement pscheck = con.prepareStatement(check);
            rs = pscheck.executeQuery();
            if(rs.isBeforeFirst()){
                String query = "delete from accountmanagement where TenTK = '" + txtTK.getText() + "'";
                ps = con.prepareStatement(query);
                ps.executeUpdate();
                int row = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
                if(row == JOptionPane.YES_OPTION){
                    JOptionPane.showMessageDialog(this, "Xóa thành công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                }
                DisplayTable();
            }
            else{
                JOptionPane.showMessageDialog(this, "Tên đăng nhập không tồn tại", "Thông Báo", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_DeleteActionPerformed


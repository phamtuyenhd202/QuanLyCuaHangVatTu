    public void DisplayTable() throws SQLException{
        ps = con.prepareStatement("Select * from accountmanagement");
        rs = ps.executeQuery();
        tableTK.setModel(DbUtils.resultSetToTableModel(rs));
        //tableTK.setModel(DbUtils.main(strings))
        String[] header = {"Tên tài khoản", "Mật Khẩu", "Quyền Đăng Nhập", "Mã Nhân Viên"};
        TableColumnModel column = (TableColumnModel)tableTK.getColumnModel();
        for(int i=0; i<header.length; i++){
            TableColumn title = column.getColumn(i);
            title.setHeaderValue(header[i]);
        }
        tableTK.getTableHeader().repaint();
    }
    
    public void HienThiMaNV() throws SQLException{
        ps = con.prepareStatement("Select MaNV from customermanagement");
        
        ArrayList<String> listMaNV = new ArrayList<>();
        rs = ps.executeQuery();
        while(rs.next()){
            listMaNV.add(rs.getString("MaNV"));
        }
        
        for(String manv : listMaNV){
            cboMaNV.addItem(manv);
        }
    }

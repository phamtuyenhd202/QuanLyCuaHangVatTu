    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        // TODO add your handling code here:
        if(txtTK.getText().isEmpty() || txtMK.getText().isEmpty() || txtXNMK.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Dữ liệu không được để trống", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        
        if(txtMK.getText().equals(txtXNMK.getText())){
            try{
                String query1 = "Select TenTK from accountmanagement where TenTK = '" + txtTK.getText() +"'";
                PreparedStatement checkTK = con.prepareStatement(query1);
                ResultSet rsTK = checkTK.executeQuery();
                String query2 = "Select MaNV from accountmanagement where MaNV = '" + cboMaNV.getSelectedItem().toString() +"'";
                PreparedStatement checkMaNV = con.prepareStatement(query2);
                ResultSet rsMaNV = checkMaNV.executeQuery();
                if(rsTK.next()){
                    JOptionPane.showMessageDialog(this, "Tên tài khoản đã tồn tại", "Thông Báo", JOptionPane.ERROR_MESSAGE);
                }
                else if(rsMaNV.next()){
                    JOptionPane.showMessageDialog(this, "Mỗi nhân viên chỉ có 1 tài khoản", "Thông Báo", JOptionPane.WARNING_MESSAGE);
                }
                else{
                    ps = con.prepareStatement("insert into accountmanagement values(?, ?, ?, ?)");
                    ps.setString(1, txtTK.getText());
                    ps.setString(2, txtMK.getText());
                    ps.setString(3, cboQuyen.getSelectedItem().toString());
                    ps.setString(4, cboMaNV.getSelectedItem().toString());
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Thêm thành công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                    DisplayTable();
                }
            }
            catch(Exception e){
                e.printStackTrace();
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Mật khẩu không đúng", "Thông Báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_AddActionPerformed


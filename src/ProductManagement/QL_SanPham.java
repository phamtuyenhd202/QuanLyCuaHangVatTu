    private void DisplayTable(){
        try{
            st = con.createStatement();
            rs = st.executeQuery("Select * from productmanagement");
            ProductTable.setModel(DbUtils.resultSetToTableModel(rs));
            String[] header = {"Mã sản phẩm", "Tên sản phẩm","Nhà cung cấp","Giá bán","Số lượng","Đơn vị tính","Hạn bảo hành", "Mã danh mục"};
            TableColumnModel tcm = (TableColumnModel)ProductTable.getColumnModel();
            for(int i=0; i<header.length; i++){
                TableColumn title = tcm.getColumn(i);
                title.setHeaderValue(header[i]);
            }
            ProductTable.getTableHeader().repaint();
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
    

    private void spAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_spAddActionPerformed
        // TODO add your handling code here:
        if(textMSP.getText().isEmpty() || textTSP.getText().isEmpty() || textTNCC.getText().isEmpty() || textGB.getText().isEmpty() || textSL.getText().isEmpty() || textDVT.getText().isEmpty() || textHBH.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Dữ liệu không được để trống", "Thông Báo", JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                ps = con.prepareStatement("insert into productmanagement values(?, ?, ?, ?, ?, ?, ?, ?)");
                PreparedStatement ps1 = con.prepareStatement("select MaDM from productportfolio where TenDM = ?");
                ps1.setString(1, cboDM.getSelectedItem().toString());
                rs = ps1.executeQuery();
                String maDM = null;
                if(rs.next()){
                    maDM = rs.getString("MaDM");
                }
                ps.setString(1, textMSP.getText());
                ps.setString(2, textTSP.getText());
                ps.setString(3, textTNCC.getText());
                ps.setDouble(4, Double.parseDouble(textGB.getText()));
                ps.setInt(5, Integer.parseInt(textSL.getText()));
                ps.setString(6, textDVT.getText());
                ps.setString(7, textHBH.getText());
                ps.setString(8, maDM);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Thêm Thành Công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                DisplayTable();
                //con.close();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this, e, "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_spAddActionPerformed


    private void DisplayDM(){
        try{
            st = con.createStatement();
            rs = st.executeQuery("Select TenDM from productportfolio");
        
            ArrayList<String> listDM = new ArrayList<String>();
            while(rs.next()){
                listDM.add(rs.getString("TenDM"));
            }
        
            for(String s : listDM){
                cboDM.addItem(s);
                searchDM.addItem(s);
            }
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }

    private void ProductTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ProductTableMouseClicked
        try{
            DefaultTableModel dtm = (DefaultTableModel)ProductTable.getModel();
            int data = ProductTable.getSelectedRow();
            textMSP.setText(dtm.getValueAt(data, 0).toString());
            textTSP.setText(dtm.getValueAt(data, 1).toString());
            textTNCC.setText(dtm.getValueAt(data, 2).toString());
            textGB.setText(dtm.getValueAt(data, 3).toString());
            textSL.setText(dtm.getValueAt(data, 4).toString());
            textDVT.setText(dtm.getValueAt(data, 5).toString());
            textHBH.setText(dtm.getValueAt(data, 6).toString());
        
            String mdm = dtm.getValueAt(data, 7).toString();
            st = con.createStatement();
            rs = st.executeQuery("Select TenDM from productportfolio where MaDM = '" + mdm + "'");
            String dm = null;
            while(rs.next()){
                dm = rs.getString("TenDM");
            }
            cboDM.setSelectedItem(dm);
        }
        catch(Exception e){
            e.printStackTrace();
        }
             
    }//GEN-LAST:event_ProductTableMouseClicked


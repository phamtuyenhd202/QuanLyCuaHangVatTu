    private void searchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchMouseClicked
        // TODO add your handling code here:
        if(searchName.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập tên nhà cung cấp!", "Thông Báo", JOptionPane.WARNING_MESSAGE);
        }
        else{
            try{
                String search = "Select * from productmanagement where TenNCC = '" + searchName.getText() + "'";
                st = con.createStatement();
                rs = st.executeQuery(search);
                if(!rs.isBeforeFirst()){
                    JOptionPane.showMessageDialog(this, "Nhà cung cấp không tồn tại", "Thông Báo" ,JOptionPane.INFORMATION_MESSAGE);
                    DisplayTable();
                }
                else{
                     JOptionPane.showMessageDialog(this, "Tìm thành công");
                     ProductTable.setModel(DbUtils.resultSetToTableModel(rs));
                }
            }
            catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_searchMouseClicked


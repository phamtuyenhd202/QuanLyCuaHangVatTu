    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here
        if(tid.getText().isEmpty() || tname.getText().isEmpty() || buttonGroup1.getSelection() == null || tbirthday.getDate() == null || taddress.getText().isEmpty() || tphone.getText().isEmpty() || temail.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Dữ liệu không được để trống", "Thông Báo", JOptionPane.WARNING_MESSAGE);
        }
        else{
            try{
                if(this.con != null){
                ps = con.prepareStatement("insert into customermanagement values (?, ?, ?, ?, ?, ?, ?, ?)");
                ps.setString(1, tid.getText());
                ps.setString(2, tname.getText());
                if (tgendermale.isSelected()) {
                    ps.setString(3, "Nam");
                } else if (tgenderfemale.isSelected()) {
                    ps.setString(3, "Nữ");
                }
                SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
                String birthdateString = dateFormat.format(tbirthday.getDate());
                ps.setString(4, birthdateString);
                ps.setString(5, taddress.getText());
                ps.setString(6, tphone.getText());
                ps.setString(7, temail.getText());
                ps.setString(8, tposition.getSelectedItem().toString());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                DisplayTable();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Lỗi", "Thông Báo", JOptionPane.ERROR_MESSAGE);
                }
            }
            catch(SQLException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_addActionPerformed


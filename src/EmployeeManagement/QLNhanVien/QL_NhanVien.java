    private void searchpositionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchpositionActionPerformed
        // TODO add your handling code here:
        try{
            String query = "Select * from customermanagement where ChucVu ='" + searchposition.getSelectedItem()+ "'";
            st = con.createStatement();
            rs = st.executeQuery(query);
            if(!rs.isBeforeFirst()){
                JOptionPane.showMessageDialog(this, "Không có nhân viên nào giữ chức vụ này!");
                DisplayTable();
            }
            else{
                JOptionPane.showMessageDialog(this, "Tìm thành công");
                tableemployee.setModel(DbUtils.resultSetToTableModel(rs));
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_searchpositionActionPerformed


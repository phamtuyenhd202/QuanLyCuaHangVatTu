    private void tableemployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableemployeeMouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) tableemployee.getModel();
        // getSelectedRow(): lấy chỉ mục của hàng đang được chọn
        int data = tableemployee.getSelectedRow();
        tid.setText(dtm.getValueAt(data, 0).toString());
        tname.setText(dtm.getValueAt(data, 1).toString());
        if(dtm.getValueAt(data, 2).toString().equals("Nam")){
            tgendermale.setSelected(true);
            tgenderfemale.setSelected(false);
        }
        else if(dtm.getValueAt(data, 2).toString().equals("Nữ")){
            tgenderfemale.setSelected(true);
            tgendermale.setSelected(false);
        }

        //SimpleDateFormat dateformnat = new SimpleDateFormat()
        try {
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
            java.util.Date date = (java.util.Date)dateFormat.parse(dtm.getValueAt(data, 3).toString());
            tbirthday.setDate(date);
        } catch (Exception e) {
            e.printStackTrace();
        }
        taddress.setText(dtm.getValueAt(data,4).toString());
        tphone.setText(dtm.getValueAt(data, 5).toString());
        temail.setText(dtm.getValueAt(data, 6).toString());
        tposition.setSelectedItem(dtm.getValueAt(data, 7).toString());
    }//GEN-LAST:event_tableemployeeMouseClicked


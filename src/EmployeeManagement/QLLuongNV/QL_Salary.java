    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            // TODO add your handling code here:
            String query = "Delete from salarymanagement where MaNV = '" + cboMaNV.getSelectedItem().toString() + "'";
            st = con.createStatement();
            int row = st.executeUpdate(query);
            if(row > 0){
                int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa ?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION );
                if(choice == JOptionPane.YES_OPTION){
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    DefaultTableModel model = (DefaultTableModel) tableSalaryNV.getModel();
                    int rowCount = model.getRowCount();
                    for (int i = rowCount - 1; i >= 0; i--) {
                        String manv = model.getValueAt(i, 1).toString();
                        if (manv.equals(cboMaNV.getSelectedItem().toString())) {
                            model.removeRow(i);
                            break;
                        }
                    }
                    txtSoNgay.setText("");
                    txtSalary.setText("");
                    txtPC.setText("");
                }
                sumLuong = 0;
                HienThiSumSalary();
            }
        } catch (SQLException ex) {
            Logger.getLogger(QL_Salary.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


    public void DisplayTable(){
        try {
            ps = con.prepareStatement("Select * from salarymanagement");
            rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel)tableSalaryNV.getModel();
            int stt = 1;
            int tongluong = 0;
            while(rs.next()){
                String manv = rs.getString("MaNV");
                String tennv = rs.getString("TenNV");
                int luongngay = rs.getInt("LuongNgay");
                int songay = rs.getInt("SoNgayLam");
                int phucap = rs.getInt("PhuCap");
                
                model.addRow(new Object[]{
                    stt,
                    manv,
                    tennv,
                    luongngay,
                    songay,
                    phucap,
                    tongluong = songay * luongngay + phucap
                });
                stt++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(QL_Salary.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

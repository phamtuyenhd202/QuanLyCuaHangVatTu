    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            // TODO add your handling code here:
            String query = "update salarymanagement set SoNgayLam = '" + txtSoNgay.getText() +
                    "', LuongNgay = '" + txtSalary.getText() +
                    "', PhuCap = '" + txtPC.getText() +
                    "' where MaNV = '" + cboMaNV.getSelectedItem().toString() + "'";
            st = con.createStatement();
            
            int row = st.executeUpdate(query);
            if(row > 0){
                JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                
                DefaultTableModel model = (DefaultTableModel) tableSalaryNV.getModel();
                int rowCount = model.getRowCount();
                for(int i=0; i<rowCount; i++){
                    String manv = model.getValueAt(i, 1).toString();
                    if(manv.equals(cboMaNV.getSelectedItem().toString())){
                        model.setValueAt(txtSalary.getText(), i, 3);
                        model.setValueAt(txtSoNgay.getText(), i, 4);
                        model.setValueAt(txtPC.getText(), i, 5);
                        model.setValueAt((Integer.parseInt(txtSalary.getText()) * Integer.parseInt(txtSoNgay.getText()) + Integer.parseInt(txtPC.getText())), i, 6);
                        break;
                    }
                }
                sumLuong = 0;
                HienThiSumSalary();
            }
        } catch (SQLException ex) {
            Logger.getLogger(QL_Salary.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


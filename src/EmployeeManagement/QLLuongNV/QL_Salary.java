    private void HienThiMaNV(){
         try{
            st = con.createStatement();
            rs = st.executeQuery("Select MaNV from customermanagement");
            
            ArrayList<String> listMaNV = new ArrayList<String>();
            while(rs.next()){
                listMaNV.add(rs.getString("MaNV"));
            }
           
            for(String s: listMaNV){
                cboMaNV.addItem(s);
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
    
    private void HienThiSumSalary(){
        DefaultTableModel model = (DefaultTableModel)tableSalaryNV.getModel();
        int rowCount = model.getRowCount();
        for(int i=0; i<rowCount; i++){
            sumLuong += Integer.parseInt(model.getValueAt(i, 6).toString());
        }
        sumSalary.setText(Integer.toString(sumLuong));
    }

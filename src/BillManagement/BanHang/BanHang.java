    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int selectRow = tableDH.getSelectedRow();
        if(selectRow != -1){
            try {
                String valueMaSP = tableDH.getValueAt(selectRow, 0).toString();
                DefaultTableModel dtm = (DefaultTableModel)tableDH.getModel();
                
                String query = "Delete from invoicedetails where MaHD = ? and MaSP = ? ";
                ps = con.prepareStatement(query);
                ps.setString(1, cboMaHD.getSelectedItem().toString());
                ps.setString(2, valueMaSP);
                int check = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa sản phẩm!!", "Thông Báo", JOptionPane.YES_NO_OPTION);
                if(check == JOptionPane.YES_OPTION){
                    ps.executeUpdate();
                    dtm.removeRow(selectRow);
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed


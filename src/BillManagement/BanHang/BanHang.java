    private void DisplayMaNV(){
        try{
            String query = "Select MaNV from customermanagement";
            ps = con.prepareStatement(query);
            rs = ps.executeQuery();
            ArrayList<String> listMaNV = new ArrayList<>();
            while(rs.next()){
                listMaNV.add(rs.getString("MaNV"));
            }
            
            for(String manv : listMaNV){
                cboMaNV.addItem(manv);
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
    private void HienThiMaHD(){
        try {
            String query = "Select MaHD from invoices";
            ps = con.prepareStatement(query);
            rs = ps.executeQuery();
            ArrayList<String> listMaHD = new ArrayList<>();
            while(rs.next()){
                listMaHD.add(rs.getString("MaHD"));
            }
            
            for(String maHD : listMaHD){
                cboMaHD.addItem(maHD);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DonHang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void HienThiTongTienHD(){
        try{
            String query = "Select Sum(SoLuongMua * DonGia) AS 'TongTien' from invoicedetails where MaHD = '"+ cboMaHD.getSelectedItem().toString() +"' group by MaHD";
            ps = con.prepareStatement(query);
            rs = ps.executeQuery();
            String sum = null;
            if(rs.isBeforeFirst()){
                sum = rs.getString("TongTien");
            }
            txtSum.setText(sum);
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
    

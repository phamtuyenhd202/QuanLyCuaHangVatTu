    private void btnXemHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXemHDActionPerformed
        try {
            // TODO add your handling code here:
            String query = "Select productmanagement.MaSP, productmanagement.TenSP, SoLuongMua, invoicedetails.DonGia, SoLuongMua * DonGia AS 'ThanhTien' from productmanagement inner join invoicedetails on productmanagement.MaSP = invoicedetails.MaSP " +
                    " inner join invoices on invoicedetails.MaHD = invoices.MaHD " +
                    " where invoices.MaHD = '" + cboMaHD.getSelectedItem().toString() + "'";
            ps = con.prepareStatement(query);
            rs = ps.executeQuery();
            if(rs.isBeforeFirst()){
                String[] header = {"Mã sản phẩm", "Tên sản phẩm", "Số lượng bán","Giá bán","Thành tiền"};
                tableDH.setModel(DbUtils.resultSetToTableModel(rs));
                String sum = "Select Sum(SoLuongMua * DonGia) AS 'TongTien' from invoicedetails where MaHD = '"+ cboMaHD.getSelectedItem().toString() +"' group by MaHD";
                PreparedStatement pssum = con.prepareStatement(sum);
                ResultSet rssum = pssum.executeQuery();
                String result = null;
                if(rssum.next()){
                    result = rssum.getString("TongTien");
                }
                txtSum.setText(result);
                TableColumnModel tcm = (TableColumnModel)tableDH.getColumnModel();
                for(int i=0; i<header.length; i++){
                    TableColumn title = tcm.getColumn(i);
                    title.setHeaderValue(header[i]);
                }
                tableDH.getTableHeader().repaint();
            }
            else{
                JOptionPane.showMessageDialog(this, "Hóa Đơn chưa được nhập", "Thông Báo", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } 
    }//GEN-LAST:event_btnXemHDActionPerformed

